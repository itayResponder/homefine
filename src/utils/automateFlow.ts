// Generates a binary .flo file for the Automate (LlamaLab) app.
// Structure reverse-engineered from a real single-HTTP-request .flo file.
// Format: HEADER + [HTTP request block per config] + FOOTER (all fixed except apiKey + URL).

const HEADER = [
  0x4c, 0x41, 0x46, 0x6c, 0x00, 0x70, 0x08, 0x04,
  0xe0, 0x10, 0x02, 0x00, 0x00, 0xd0, 0x12, 0x04,
  0x00, 0x0c, 0xfe, 0x10, 0x06, 0x00, 0x18, 0x03, 0x00,
]

const FOOTER = [
  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x20, 0x02,
  // notification filter package: com.google.android.apps.walletnfcrel (36 bytes)
  0xd4, 0x01, 0x24,
  0x63, 0x6f, 0x6d, 0x2e, 0x67, 0x6f, 0x6f, 0x67, 0x6c, 0x65, 0x2e,
  0x61, 0x6e, 0x64, 0x72, 0x6f, 0x69, 0x64, 0x2e, 0x61, 0x70, 0x70,
  0x73, 0x2e, 0x77, 0x61, 0x6c, 0x6c, 0x65, 0x74, 0x6e, 0x66, 0x63,
  0x72, 0x65, 0x6c,
  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x15,
  0x1b, 0x25, 0x00, 0x00, 0x00,
  0x2b, 0x31,
  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x03,
  0x05, 0xde, 0x13,
  0x08, 0x00, 0x24, 0x03, 0x03,
  0xd0, 0x01, 0x40,
  0x08, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
  0x00, 0x00,
]

function strBytes(s: string): number[] {
  return Array.from(new TextEncoder().encode(s))
}

function d4Str(s: string): number[] {
  return [0xd4, 0x01, s.length, ...strBytes(s)]
}

// HTTP body field with an Automate variable reference: key → {varName}
function varField(key: string, varName: string): number[] {
  return [
    ...d4Str(key),
    0xd2, 0x01, 0x02, 0x00, 0x00,
    0xcc, 0x01, varName.length, ...strBytes(varName),
    0x00, 0x01,
  ]
}

// HTTP body field with a literal string value: key → value
function literalField(key: string, value: string): number[] {
  return [...d4Str(key), ...d4Str(value), 0x01]
}

function requestBlock(apiKey: string, webhookUrl: string): number[] {
  const urlBytes = strBytes(webhookUrl)
  return [
    // URL
    0xd4, 0x01, urlBytes.length, ...urlBytes,
    // Method: POST + trailing 0x00
    0xd4, 0x01, 0x04, 0x50, 0x4f, 0x53, 0x54, 0x00,
    // Timeout / config block (fixed)
    0xd0, 0x01, 0x40, 0x4e, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // Content-Type: application/json
    ...d4Str('application/json'),
    // Separator
    0x8a, 0x02, 0x06,
    // Body fields — order matches handleCopyBody JSON
    ...varField('title', 'notifTitle'),
    ...varField('body', 'notifText'),
    ...literalField('apiKey', apiKey),
    ...varField('ticker', 'notifTicker'),
    ...varField('timestamp', 'notifTimestamp'),
    ...varField('extras', 'notifExtras'),
  ]
}

export function generateAutomateFlowBinary(
  configs: { apiKey: string }[],
  webhookUrl: string
): Uint8Array<ArrayBuffer> {
  const bytes = [...HEADER, ...configs.flatMap(cfg => requestBlock(cfg.apiKey, webhookUrl)), ...FOOTER]
  const buf = new ArrayBuffer(bytes.length)
  const view = new Uint8Array(buf)
  view.set(bytes)
  return view
}
